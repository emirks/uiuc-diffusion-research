"""exp_061 — deterministic item selection from frozen split v1 (post-audit).

Rule (frozen here; metadata-only, no metric scores):

  1. For every class WITH test clips in data/processed/transitions_std121/
     split_v1.json: items = the class's test clips (sorted) — the endpoints
     source for R1 and the prompt source for both arms.
  2. For all-train classes (no test clips): one item = the class's BAR-PAIR
     endpoints clip (clip A = generated_video of sib__<class>), read verbatim
     from the certified certification artifact
     outputs/eval/certification/3.0.0-draft.8/manifests/siblings.json —
     exactly the exp_060 pattern. Classes absent from siblings.json (the n=1
     singletons jump_transition, seamless_transition) use their single clip.
  3. Captions: reused from exp_056/057/058 caption files + exp_060
     captions_extra.json + exp_061 captions_extra.json; any still missing is
     generated by caption_missing.py (exact exp_058/060 captioner).

Writes dataset/selection.json. Login-node / CPU only, stdlib.
"""

import hashlib
import json
import pathlib

REPO_ROOT = pathlib.Path(__file__).resolve().parents[2]
EXP = pathlib.Path(__file__).resolve().parent
SPLIT = REPO_ROOT / "data/processed/transitions_std121/split_v1.json"
SIBLINGS = REPO_ROOT / "outputs/eval/certification/3.0.0-draft.8/manifests/siblings.json"
CAPTION_FILES = [
    "experiments/exp_056_ltx2_ic_lora_transition_transfer/dataset/captions.json",
    "experiments/exp_057_ic_lora_unseen_eval/dataset/captions.json",
    "experiments/exp_058_ic_lora_diverse_retrain/dataset/captions.json",
    "experiments/exp_060_sigma_seed/dataset/captions_extra.json",
    "experiments/exp_061_ladder_r0_r1/dataset/captions_extra.json",
]

SEEDS = [42, 43, 44]


def main() -> None:
    split_bytes = SPLIT.read_bytes()
    split = json.loads(split_bytes)
    sib = {r["item_id"][len("sib__"):]: pathlib.Path(r["generated_video"]).stem
           for r in json.loads(SIBLINGS.read_text())}
    captions = {}
    for f in CAPTION_FILES:
        p = REPO_ROOT / f
        if p.exists():
            captions.update(json.loads(p.read_text()))

    items = []
    for cls in sorted(split["classes"]):
        v = split["classes"][cls]
        if v["test"]:
            picks = [(c, "split_v1_test") for c in sorted(v["test"])]
        elif cls in sib:
            picks = [(sib[cls], "bar_pair")]
        else:
            assert v["n_clips"] == 1, f"{cls}: no test, no bar pair, n>1"
            picks = [(sorted(v["train"])[0], "singleton")]
        for clip, source in picks:
            items.append({
                "class": cls,
                "clip": clip,
                "clip_rel": v["paths"][clip],
                "source": source,
                "n_clips": v["n_clips"],
                "caption": captions.get(clip),  # None -> caption_missing.py fills
            })

    out = {
        "selection_rule": __doc__,
        "seeds": SEEDS,
        "split_v1": str(SPLIT.relative_to(REPO_ROOT)),
        "split_v1_sha256": hashlib.sha256(split_bytes).hexdigest(),
        "siblings_artifact": str(SIBLINGS.relative_to(REPO_ROOT)),
        "n_items": len(items),
        "items": items,
    }
    (EXP / "dataset/selection.json").write_text(json.dumps(out, indent=2))
    n_missing = sum(1 for it in items if not it["caption"])
    print(f"[done] {len(items)} items -> dataset/selection.json "
          f"({n_missing} missing captions)")
    for it in items:
        cap = "cap:OK" if it["caption"] else "cap:MISSING"
        print(f"  {it['class']:24s} {it['clip']:28s} [{it['source']:13s}] {cap}")


if __name__ == "__main__":
    main()
